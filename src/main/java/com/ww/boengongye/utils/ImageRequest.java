package com.ww.boengongye.utils;

public class ImageRequest {
    private String project;
    private String whiteShift;
    private String nightShift;
    private String totalShift;

    public String getProject() {
        return project;
    }

    public void setProject(String project) {
        this.project = project;
    }

    public String getWhiteShift() {
        return whiteShift;
    }

    public void setWhiteShift(String whiteShift) {
        this.whiteShift = whiteShift;
    }

    public String getNightShift() {
        return nightShift;
    }

    public void setNightShift(String nightShift) {
        this.nightShift = nightShift;
    }

    public String getTotalShift() {
        return totalShift;
    }

    public void setTotalShift(String totalShift) {
        this.totalShift = totalShift;
    }
}
