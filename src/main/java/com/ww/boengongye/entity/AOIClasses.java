package com.ww.boengongye.entity;

public class AOIClasses {
    public String classid;
    public String classname;
    public String classname_en;
    public String symbolchar;

    public AOIClasses() {
    }

    public String getClassid() {
        return classid;
    }

    public void setClassid(String classid) {
        this.classid = classid;
    }

    public String getClassname() {
        return classname;
    }

    public void setClassname(String classname) {
        this.classname = classname;
    }

    public String getClassname_en() {
        return classname_en;
    }

    public void setClassname_en(String classname_en) {
        this.classname_en = classname_en;
    }

    public String getSymbolchar() {
        return symbolchar;
    }

    public void setSymbolchar(String symbolchar) {
        this.symbolchar = symbolchar;
    }
}
