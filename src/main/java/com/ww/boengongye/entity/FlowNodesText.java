package com.ww.boengongye.entity;

public class FlowNodesText {
    public Integer x;
    public Integer y;
    public String value;

    public FlowNodesText() {
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
